export class Persona {

  constructor(id, nombre, apellido, direccion, telefono, edad, genero, cedula, adopciones) {
    this.id = id;
    this.nombre = nombre;
    this.apellido = apellido;
    this.direccion = direccion;
    this.telefono = telefono;
    this.edad = edad;
    this.genero = genero;
    this.cedula = cedula;
    this.adopciones = adopciones;
  }
  
}