export class Persona {
     
    constructor(id, nombre, apellido, cedula, genero, direcccion, telefono, edad) {
        this.id = id;
        this.nombre = nombre;
        this.apellido = apellido;
        this.cedula = cedula;
        this.genero = genero;
        this.direcccion = direcccion;
        this.telefono = telefono;
        this.edad = edad;
    }

}